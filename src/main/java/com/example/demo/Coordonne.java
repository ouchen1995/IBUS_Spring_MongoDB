package com.example.demo;

public class Coordonne {

	private float X,Y;

	
	public Coordonne(int x,int y)
	{
		X=x;
		Y=y;
	}
	
	public Coordonne()
	{
		X=0; Y=0;
	}
	
	
	
	public float getX() {
		return X;
	}

	public void setX(float x) {
		X = x;
	}

	public float getY() {
		return Y;
	}

	public void setY(float y) {
		Y = y;
	}
	
	
	
	
	
}
